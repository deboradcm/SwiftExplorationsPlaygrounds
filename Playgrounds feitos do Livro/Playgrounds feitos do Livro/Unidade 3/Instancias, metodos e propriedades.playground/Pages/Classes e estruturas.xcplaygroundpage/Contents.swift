 Quando estiver desenvolvendo apps na linguagem Swift, você trabalhará com instâncias de _estruturas_ e _classes_. As duas são usadas para definir tipos nessa linguagem.
 
 Por ora, você só precisa saber que as estruturas e as classes têm muitas semelhanças:

- As duas têm instâncias
- As instâncias são criadas com um inicializador
- As duas podem ter métodos
- As duas podem ter propriedades

Para criar e usar instâncias, você formulará o mesmo código na linguagem Swift, não importando se o tipo for estrutura ou classe.
 
Durante este curso, você colocará em prática a criação de instâncias e o uso de métodos e propriedades. Em uma das aulas, aprenderá também a definir suas próprias estruturas. Não vamos explorar as diferenças entre classes e estruturas aqui, mas, quando você ganhar experiência no uso das duas, estará preparado para aprender essas diferenças.

