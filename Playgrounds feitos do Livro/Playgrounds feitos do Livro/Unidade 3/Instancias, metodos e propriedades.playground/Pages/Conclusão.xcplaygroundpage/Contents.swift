## Conclusão

 Nesta aula, você aprendeu que cada valor de um determinado tipo é chamado de instância.
 
 Aprendeu também que os tipos não são apenas rótulos, como `String` ou `Int`, mas que cada tipo pode ter outros recursos.
 
 Os tipos podem ter propriedades, que permitem que cada instância tenha um conjunto de valores.
 
 Os tipos podem ter métodos de instância, que são funções definidas como parte de um tipo.
 
 Para saber quais são os métodos e as propriedades disponíveis, use a documentação integrada ao Xcode. Ela pode ser acessada diretamente no código: basta clicar no código mantendo pressionada a tecla Option ou usar o inspetor da ajuda rápida. Outra opção é abrir o navegador de documentação em outra janela e procurar o que for do seu interesse.
 
 Você viu que até mesmo um tipo que parece simples, como `String`, tem vários métodos e propriedades.
 
 Você também já aprendeu por que é bom usar métodos de instância e propriedades em vez de funções definidas no primeiro nível.
 
