/*:
 O BoogieBot pode fazer estes passos:
- `leftArmUp()`, `leftArmDown()`, `rightArmUp()`, `rightArmDown()`
- `leftLegUp()`, `leftLegDown()`, `rightLegUp()`, `rightLegDown()`
- `shakeItLeft()`, `shakeItRight()`, `shakeItCenter()`
- `jumpUp()`, `jumpDown()`
- `fabulize()`, `defabulize()`

 Os passos estão bem claros: [`leftArmUp()` – levantar braço esquerdo; `leftArmDown()` – abaixar braço esquerdo; `rightArmUp()` – levantar braço direito; `rightArmDown()` – abaixar braço direito; `leftLegUp()` – levantar perna esquerda; `leftLegDown()` – abaixar perna esquerda; `rightLegUp()` – levantar perna direita; `rightLegDown()` – abaixar perna direita; `shakeItLeft()` – sacudir para a esquerda; `shakeItRight()` – sacudir para a direita; `shakeItCenter()` – sacudir ao centro; `jumpUp()` – pular para cima; `jumpDown()` – pular para baixo], exceto `fabulize()` e `defabulize()`. O passo `fabulize()` deixa o BoogieBot com novas cores fabulosas, e o passo `defabulize()` faz com que ele volte a ter uma cor cinza sem graça de robôs.
 
 É assim que você manda o BoogieBot se mexer:

```
fabulize()
shakeItLeft()
shakeItRight()
shakeItCenter()
```

 O BoogieBot para ao terminar a coreografia. Para ver os passos novamente, selecione Editor > Execute Playground (Editor > Executar playground). Se você mudar a coreografia, precisará começar tudo de novo.
