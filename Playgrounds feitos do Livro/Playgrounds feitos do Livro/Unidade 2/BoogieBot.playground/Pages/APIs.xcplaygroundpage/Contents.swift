## APIs

 Os desenvolvedores de apps sempre dependem de códigos formulados por outras pessoas. No caso do BoogieBot, várias funções são definidas para fazer com que ele seja iniciado, se movimente e mude de cor.
 
 O conjunto de funções que controlam o BoogieBot é um exemplo de _Interface de Programação de Aplicativos_ mais conhecida pela sigla em inglês de _Application Programming Interface_, _API_.
 
 Uma API é um conjunto específico de funcionalidades que um programador de software pode usar para realizar uma tarefa. Nesse caso, a API do BoogieBot faz um robô de software dançar.

### API do BoogieBot
 Para iniciar o BoogieBot:
- `startBot()`

 Para mover o BoogieBot:

- `leftArmUp()`, `leftArmDown()`, `rightArmUp()`, `rightArmDown()`
- `leftLegUp()`, `leftLegDown()`, `rightLegUp()`, `rightLegDown()`
- `shakeItLeft()`, `shakeItRight()`, `shakeItCenter()`
- `jumpUp()`, `jumpDown()`

 Para alterar a cor do BoogieBot:
- `fabulize()`, `defabulize()`

 A seguir, aprenda a juntar essas funções para criar coreografias.
 
