## Trocar de roupa

 Imagine que um amigo convide você para ir ao cinema. Você acordou tarde, então responde: “Encontro você lá. Só preciso trocar de roupa”.
 
 Seu amigo sabe o que você quer dizer com “trocar de roupa”, mas ele desconhece detalhes, como a camiseta que você vai vestir.
 
 Agora, pense nos detalhes de tudo o que você fez para trocar de roupa hoje: decidir o que vestir, pegar as roupas no armário e vestir cada peça.
 
 O termo “trocar de roupa” parece simples, mas, quando pensamos em todos os detalhes envolvidos na tarefa, entendemos que há uma grande complexidade.

 Usar uma frase como “trocar de roupa” é uma forma de se referir ao processo de se vestir sem precisar contar cada detalhe específico.

 As pessoas fazem isso o tempo todo. Frases como “ir ao dentista”, “fazer um sanduíche” ou “tomar um café” descrevem atividades com etapas que seriam tediosas de explicar detalhadamente.
 
 Na Unidade 1, você aprendeu sobre *abstração*: a ideia de definir uma forma mais simples de abordar as complexidades para não precisar pensar nos detalhes. Esse conceito fundamental do desenvolvimento de software é a maneira de os programadores evitarem ficar sobrecarregados por detalhes e complexidades.

 Nesta aula, você aprenderá a definir e usar _funções_, uma das formas mais básicas de definir a abstração na programação.

 - callout(Experimente): Pense em outras frases simples que representam atividades ou ideias muito mais complicadas.

