## Como invocar funções

 Você já aproveitou a abstração e as funções quando usou `print()` para exibir “Olá, mundo!” no console. Na linguagem Swift, `print()` é uma função. Quando é usada, dizemos que você está _invocando_ a função:
 */
print("Olá, mundo!")
print(360)
/*:
Assim como você realiza muitas tarefas ao trocar de roupa, muitas coisas acontecem quando a função `print()` é invocada, inclusive:
- Transformar em string tudo o que você passa a ela, inclusive números.
- Adicionar um caractere de nova linha, de forma que cada invocação de `print()` termine em uma linha nova.
- Fazer com que essa string apareça no console.

Nesse caso, você invoca uma função criada por outra pessoa. Não é necessário saber todos os detalhes sobre como `print()` funciona para invocá-la.

 A reutilização é um dos principais motivos que tornam as funções tão poderosas. Com elas, é possível combinar etapas detalhadas em uma definição que pode ser usada muitas vezes.

No restante deste playground, você praticará a invocação de funções e aprenderá a definir suas próprias funções.
 
