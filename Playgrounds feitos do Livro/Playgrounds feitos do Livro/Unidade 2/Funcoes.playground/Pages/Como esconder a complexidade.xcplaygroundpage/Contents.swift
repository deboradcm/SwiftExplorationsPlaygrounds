## Como esconder a complexidade

 Exibir a cantiga no console usando funções pode parecer mais demorado e complicado que o código original na primeira página deste playground. É verdade se você pensar em cada uma das funções ao mesmo tempo. Mas a ideia de usar funções é dividir as coisas em partes que podem ser entendidas e reutilizadas. Cada parte realiza uma tarefa clara.

