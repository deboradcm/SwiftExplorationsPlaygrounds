/*:
## Declaração de variáveis

 Declarar uma variável é quase exatamente como declarar uma constante. No entanto, em vez de usar a palavra-chave `let`, você usa a palavra-chave `var`:
 */
// Declaração de uma constante
let placeOfBirth = "Nova Jersey"

// Declaração de uma variável
var currentLocation = "Nova Jersey"

//:  Depois de declarar uma variável, você pode atribuir um novo valor a ela:
// Atribuição de um novo valor a uma variável
currentLocation = "Califórnia"

