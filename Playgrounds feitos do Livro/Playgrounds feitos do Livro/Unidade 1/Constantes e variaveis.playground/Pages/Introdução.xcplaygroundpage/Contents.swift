/*:
Você já deve ter começado a perceber um padrão. Grande parte da programação é criar itens, dar nome a eles e depois invocar esses itens pelo nome para usá-los.
 
 Até agora, você usou constantes sempre que precisou atribuir valor a um nome. Mas ainda não respondemos a uma pergunta muito importante: por que elas são chamadas de constantes?
 
 Talvez você já tenha adivinhado que o nome “constante” tem a ver com itens que permanecem iguais (ou seja, constantes) ao longo do tempo. Assim que o valor de uma constante é atribuído, você pode ter certeza de que ele vai continuar sempre o mesmo.
 
 Existe outra maneira de associar um valor a um nome na linguagem Swift. Esses valores são chamados de _variáveis_. Com uma variável, é possível alterar o valor durante a execução do programa. Ou seja, o valor associado ao nome pode mudar (ou variar) ao longo do tempo.
 
 Neste playground, você aprenderá mais sobre as diferenças entre variáveis e constantes, como declarar variáveis e como decidir se deve usar uma variável em vez de uma constante.

 Como tudo isso funciona e por que é importante?
 
