## Mudanças confusas

Quando algo muda na sua vida, você pode vivenciar algum grau de confusão.
 
Imagine que você encontrará um grupo de seis amigos no cinema às 20h.

Você recebe uma mensagem dizendo que o plano foi mudado. Agora, vocês verão um filme diferente em outro cinema, e o encontro será às 21h.

Um pouco mais tarde, você recebe outra mensagem dizendo que os planos mudaram mais uma vez. Agora, vocês assistirão a um filme na casa de um dos amigos. O horário mudou para 18h, e vocês pedirão pizza.
 
Depois, você recebe outra mensagem, dizendo que vocês se reunirão na casa de outro amigo, às 18h30.

 - callout(Experimente): Com todas essas mudanças, qual é a probabilidade de que todos estejam no lugar certo na hora certa?\
\
Seria menos provável que houvesse erros se os planos não tivessem mudado?

Sempre que você faz uma mudança, existe a possibilidade de que alguém acabe usando a informação antiga e cometa um erro.
 
Isso também acontece na programação. Usando uma constante, você garante que um valor nunca vai mudar. Não existe a possibilidade de que outra parte do programa fique dessincronizada ou use um valor antigo.
 
 Mas, às vezes, é necessário usar variáveis. À medida que um programa é executado, as coisas vão mudando. Em programação, os itens que têm capacidade de mudar são chamados de _mutáveis_, palavra que vem do latim _mutare_, que significa “transformar-se”.
 
 Já os itens que não têm capacidade de mudar são chamados de _imutáveis_. Se não for necessário mudar algum item, é uma boa prática usar uma constante. Você reduzirá a possibilidade de haver confusão e erros.

 Falando em erros, continue para ver um erro comum ao usar constantes e variáveis.
 
